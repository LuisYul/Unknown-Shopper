package com.bigoose.unkshopper.model;

public class TipoCriterio {

    private String nombre;
    private String idCriterio;
    private String estado;
    private String idTipoCriterio;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getIdCriterio() {
        return idCriterio;
    }

    public void setIdCriterio(String idCriterio) {
        this.idCriterio = idCriterio;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getIdTipoCriterio() {
        return idTipoCriterio;
    }

    public void setIdTipoCriterio(String idTipoCriterio) {
        this.idTipoCriterio = idTipoCriterio;
    }
}
